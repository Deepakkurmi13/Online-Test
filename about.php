<h4  style="margin-left:20px;font-size:25px;color:#8f6b6b;text-decoration:underline;">purpose</h4>
<pre style="margin-left:20px;font-size:15px;color:gray;margin-top:30px;">

The role of a Learning Management System varies depending on the organization’s 
objectives, onlinetraining strategy, and desired outcomes.However, the most common use 
for LMS software is to deploy andtrack online training initiatives. Typically, assets 
are uploaded to the Learning Management System, which makes them easily accessible for 
remote learners. In some cases, the LMS can even have built-in eLearning authoring tools
that allow you to develop online training materials without additional third-party software.
Think of a Learning Management System as a vast repository where you can store and track 
information.Anyone with a login and password can access these online training resources 
whenever and wherever. For self-hosted Learning Management Systems, users must also have 
the learning management software installed on their hard drive or access to the company’s server.
Whatever the installation option, the thing to bear in mind is that LMS users fall 
into two categories: First, online learners who use the Learning Management System to participate
in online training courses; second, your eLearning team who relies on the LMS platform to disburse 
information and update the online training content
