Companies and education usually want to test a specific area
of knowledge rather than general knowledge. In this way, quizzes
are an engaging method for companies and schools to measure growth in 
knowledge, abilities, and/or skills. Typically, a quiz is a short test
and doesn’t have as great an impact on grades as a test. Some teachers might
not include the quiz score to determine the overall grade at all. Quizzes are
often given by teachers several times throughout a course. It’s an easy 
way to keep track of progress from both perspectives because it gives teachers
and students a moment of reflection. Students can find the subjects they 
need to focus on, and teachers gain insight into the knowledge gaps. There are 
different kinds of questions that can be used for quizzes. Some examples 
include fill-in-the blank, multiple choice, and true or false questions. It’s even 
possible to have images as answers.
